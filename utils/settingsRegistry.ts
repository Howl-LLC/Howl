// Settings Registry
// Central metadata for every setting in every settings tab.
// Used by the settings-search feature to match queries, highlight
// results, and scroll to the correct `setting-${id}` anchor.
//
// Contract:
//  • Every `id` is globally unique (kebab-case).
//  • Entries are sorted by tab.
//  • Radio / dropdown options are folded into the parent's `keywords`.
//  • No runtime side-effects — this file is pure data.

// Types

export type SettingType =
  | 'toggle'
  | 'dropdown'
  | 'radio'
  | 'slider'
  | 'text-input'
  | 'button'
  | 'link'
  | 'color-picker'
  | 'keybind'
  | 'other';

export type TabId =
  | 'my-account'
  | 'content-social'
  | 'data-privacy'
  | 'authorized-apps'
  | 'activity-sharing'
  | 'showcase'
  | 'devices'
  | 'connections'
  | 'notifications'
  | 'family-center'
  | 'encryption'
  | 'howl-pro'
  | 'server-boost'
  | 'subscriptions'
  | 'gift-inventory'
  | 'billing'
  | 'appearance'
  | 'accessibility'
  | 'voice-video'
  | 'chat'
  | 'keybinds'
  | 'game-overlay'
  | 'streamdeck'
  | 'language-time'
  | 'streamer-mode'
  | 'advanced';

export interface TabMeta {
  labelKey: string;
  label: string;
}

export interface SettingEntry {
  id: string;
  tab: TabId;
  labelKey: string;
  label: string;
  descriptionKey: string;
  description: string;
  type: SettingType;
  keywords: string[];
}

